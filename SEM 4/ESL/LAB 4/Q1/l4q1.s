Reset_Handler
		LDR R0,=NUM
		LDR R1,[R0]
		LDR R0,=DST
		MOV R3,#1
		MOV R4,#0xA
		MOV R5,#0
		MOV R7,#0xF
UP		MOV R6,R1
		AND R6,R7
		MLA R5,R6,R3,R5
		MUL R3,R4
		LSR R1,#4
		CMP R1,#0
		BNE UP
		STR R5,[R0]
STOP	B STOP
